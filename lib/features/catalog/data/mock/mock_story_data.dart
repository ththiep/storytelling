  required String status,
  required bool isFavorite,
  required int progressSeconds,
  required List<StoryPage> pages,
  required List<StoryVoice> voices,
  DateTime? lastPlayedAt,
}) {
  return StoryDetail(
    id: id,
    title: title,
    author: author,
    overview: overview,
    level: level,
    durationMinutes: durationMinutes,
    imageUrl: imageUrl,
